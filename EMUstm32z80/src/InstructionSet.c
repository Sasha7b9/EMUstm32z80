int LD_DD_NN(void)
{
    DD_45(prevPC) = PC16andInc();

    return 10;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int LD_pNN_HL(void)
{
    uint16 address = PC16andInc();

    RAM[address] = L;
    RAM[address + 1] = H;

    return 16;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int LD_SP_HL(void)
{
    SP = HL;

    return 6;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int PUSH_QQ(void)
{
    RAM[SP - 2] = (uint8)QQ_45(prevPC);
    RAM[SP - 1] = (uint8)(QQ_45(prevPC) >> 8);
    SP -= 2;

    return 11;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int POP_QQ(void)
{
    QQ_45(prevPC) = RAM[SP] + RAM[SP + 1] * 256;
    SP += 2;

    return 10;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int EX_DE_HL(void)
{
    uint16 temp;

    EXCH(DE, HL);

    return 4;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int EX_AF_AFalt(void)
{
    uint8 temp;

    EXCH(A, Aalt);
    EXCH(F, RFalt);

    return 4;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int EXX(void)
{
    uint8 temp;

    EXCH(B, Balt);
    EXCH(C, RCalt);
    EXCH(D, RDalt);
    EXCH(E, REalt);
    EXCH(H, RHalt);
    EXCH(L, RLalt);

    return 4;
}


//------------------------------------------------------------------------------------------------------------------------------------------------------
int EX_pSP_HL(void)
{
    uint8 temp;

    EXCH(H, RAM[SP + 1]);
    EXCH(L, RAM[SP]);

    return 9;
}


